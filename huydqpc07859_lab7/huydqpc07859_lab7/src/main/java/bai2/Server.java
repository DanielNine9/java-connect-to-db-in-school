    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        // TODO add your handling code here:
        try {
            model.addElement("Server connecting...");
            lsHistory.setModel(model);
            serverSocket = new ServerSocket(Integer.parseInt(txtPort.getText()));
            JOptionPane.showMessageDialog(this, "Đang chờ client kết nối, vui lòng chờ trong giây lát!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            socket = serverSocket.accept();
            JOptionPane.showMessageDialog(this, "Đã kết nối thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            model.addElement("Server is connected");
            lsHistory.setModel(model);
            new Thread(this).start();
        } catch (BindException ex) {
            JOptionPane.showMessageDialog(this, "Port đã được sử dụng, vùi lòng chọn port khác!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_btnStartActionPerformed

    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        try {
            output = new DataOutputStream(socket.getOutputStream());
            output.writeUTF(this.txtMessage.getText());
            model.addElement("Server say: " + this.txtMessage.getText());
            this.txtMessage.setText("");
            this.txtMessage.requestFocus();
            lsHistory.setModel(model);
            output.flush();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSendActionPerformed

    private void txtPortKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPortKeyTyped
        // TODO add your handling code here:
        if (!Character.isDigit(evt.getKeyChar())) {
            evt.consume();
        }
    }//GEN-LAST:event_txtPortKeyTyped

