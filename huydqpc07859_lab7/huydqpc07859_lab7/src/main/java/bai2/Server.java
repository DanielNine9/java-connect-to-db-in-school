    @Override
    public void run() {
        try {
            input = new DataInputStream(socket.getInputStream());
            while (true) {
                if (socket != null) {
                    model.addElement("Client say: " + input.readUTF());
                    lsHistory.setModel(model);
                }
                try {
                    Thread.sleep(1000);
                } catch (InterruptedException interruptedException) {
                    interruptedException.printStackTrace();
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Client đã rời, Server ngắt kết nối", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
//            ex.printStackTrace();
        }
    }

