    private void resetForm() {
        txtId.setText("");
        txtId.setEditable(true);
        txtName.setText("");
        txtPrice.setText("");
        txtPrice.setText("");
        txtSource.setText("");
        cboDonvi.setSelectedIndex(0);

        txtId.setBorder(gray);
        txtName.setBorder(gray);
        txtPrice.setBorder(gray);
        txtSource.setBorder(gray);

        txtId.requestFocus();

    }

    private boolean validateProduct() {
        String name = txtName.getText();
        String stringPrice = txtPrice.getText();
        String id = txtId.getText();
        String source = txtSource.getText();
        String error = "";

        if (source.isEmpty()) {
            error += "Nguồn gốc không được để trống\n";
            txtSource.setBorder(red);
            txtSource.requestFocus();
        }

        if (name.isEmpty()) {
            error += "Tên sản phẩm không được để trống\n";
            txtName.setBorder(red);
            txtName.requestFocus();
        }

        if (stringPrice.isEmpty()) {
            error += "Giá không được để trống\n";
            txtPrice.setBorder(red);
            txtPrice.requestFocus();
        }

        if (id.isEmpty()) {
            error += "Mã sản phẩm không được để trống\n";
            txtId.setBorder(red);
            txtId.requestFocus();

        }

        if (error != "") {
            showError(error);
            return false;
        }

        double price = Double.parseDouble(stringPrice);
        if (price <= 0) {
            error += "Giá phải lớn hơn hoặc bằng 0";
            txtPrice.setBorder(red);
            txtPrice.requestFocus();
        }

        if (error != "") {
            showError(error);
            return false;
        }

        return true;
    }

    
    private Product createProduct() {
        return new Product(txtId.getText(),
                txtName.getText(),
                txtSource.getText(),
                (String) cboDonvi.getSelectedItem(),
                Double.parseDouble(txtPrice.getText())
        );
    }

