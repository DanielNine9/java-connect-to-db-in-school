    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String id = JOptionPane.showInputDialog("Nhập mã sản phẩm cần xóa?");
        if (id != null) {
            if (!id.isEmpty()) {
                int opt = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa sản phẩm có mã " + id);
                if (opt == 0) {
                    Optional<Product> prd = this.list.find(id);
                    if (prd.isPresent()) {
                        if (list.delete(prd.get())) {
                            showSuccess("Xóa thành công sản phẩm");
                            if (txtId.getText().equals(id)) {
                                resetForm();
                            }
                            updateTable();
                        } else {
                            showError("Không tìm thầ sản phẩm có mã " + id);
                        }
                    } else {
                        showError("Không tìm thầ sản phẩm có mã " + id);
                    }
                }
            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductMouseClicked
        // TODO add your handling code here:
        resetForm();
        JTable table = (JTable) evt.getSource();
        int row = table.rowAtPoint(evt.getPoint());
        String id = table.getValueAt(row, 0).toString();
        Optional<Product> prd = this.list.find(id);
        if (prd.isPresent()) {
            Product result = prd.get();
            txtId.setEditable(false);
            txtId.setText(result.getId());
            txtName.setText(result.getName());
            txtPrice.setText(result.getPrice() + "");
            cboDonvi.setSelectedItem(result.getDonVi());
            txtSource.setText(result.getSource());
        }


    }//GEN-LAST:event_tblProductMouseClicked

    private void updateTable() {
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        model.setRowCount(0);

        for (Product prod : list.getList()) {
            model.addRow(new Object[]{prod.getId(), prod.getName(), String.valueOf(prod.getDonVi()), prod.getPrice(), prod.getSource()});
        }

        tblProduct.setModel(model);
    }

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (!txtId.isEditable()) {
            showWarn("Vui lòng thực hiện xong quá trình update hoặc nhấn làm mới để thêm mới nhân viên");
            return;
        }
        if (validateProduct()) {
            if (this.list.add(createProduct())) {
                showSuccess("Thêm thành công sản phẩm");
                updateTable();
                resetForm();
            } else {
                showError("Mã sản phẩm đã trùng");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        if (txtId.getText().isEmpty()) {
            showError("Vui lòng chọn sản phẩm cần update");
        } else {
            if (validateProduct()) {
                if (list.update(createProduct())) {
                    showSuccess("Update thành công sản phẩm có mã là " + txtId.getText());
                    updateTable();
                    resetForm();
                } else {
                    showError("Không tìm thấy sản phẩm có mã " + txtId.getText());
                }
            }

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

