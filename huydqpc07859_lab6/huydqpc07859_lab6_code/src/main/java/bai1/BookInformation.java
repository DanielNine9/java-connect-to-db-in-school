    private void txtFindKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFindKeyReleased
        // TODO add your handling code here:
        ArrayList<Book> list = (ArrayList<Book>) bookRepository.getBooks();
        this.btnDelete.setEnabled(false);
        if (list.size() > 0) {
            list = (ArrayList<Book>) list.stream().filter(e -> e.getTitle().toLowerCase().contains(txtFind.getText().toLowerCase())).collect(Collectors.toList());
            updateTableWhenFind(list);
        }
    }//GEN-LAST:event_txtFindKeyReleased

    public void updateTable() {
        ArrayList<Book> list = (ArrayList<Book>) bookRepository.getBooks();
        DefaultTableModel model = (DefaultTableModel) tblBook.getModel();
        model.setRowCount(0);
        if (list.size() > 0) {
            for (Book book : list) {
                model.addRow(new Object[]{book.getId(), book.getTitle(), book.getPrice()});
            }
            tblBook.setModel(model);
        }
    }

    public void updateTableWhenFind(ArrayList<Book> list) {
        DefaultTableModel model = (DefaultTableModel) tblBook.getModel();
        model.setRowCount(0);
        if (list.size() > 0) {
            for (Book book : list) {
                model.addRow(new Object[]{book.getId(), book.getTitle(), book.getPrice()});
            }

            tblBook.setModel(model);
        }
    }

