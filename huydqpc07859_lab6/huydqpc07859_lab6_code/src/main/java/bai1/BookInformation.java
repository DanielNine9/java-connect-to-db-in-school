    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblBook.getSelectedRow();
        String id = tblBook.getValueAt(row, 0).toString();
        if (row >= 0) {
            boolean check = showConfirm("Do you want delete user has id is " + id);
            if (check) {
                boolean success = bookRepository.deleteBook(id);
                if (success) {
                    showSuccess("Delete successfully");
                    this.btnDelete.setEnabled(false);
                    updateTable();
                } else {
                    showError("Id not found");
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

