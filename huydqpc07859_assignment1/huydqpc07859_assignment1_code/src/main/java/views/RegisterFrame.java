    private void validateAccount() {
        String username = this.txtUsername.getText();
        String password = new String(this.txtPassword.getPassword());
        String confirm = new String(this.txtConfirm.getPassword());
        String error = "";

        if (confirm.isEmpty()) {
            error += "Confirm is not empty\n";
            txtConfirm.setBorder(redBorder);
            txtConfirm.requestFocus();
        }

        if (password.isEmpty()) {
            error += "Password is not empty\n";
            txtPassword.setBorder(redBorder);
            txtPassword.requestFocus();
        }

        if (username.isEmpty()) {
            error += "Username is not empty\n";
            txtUsername.setBorder(redBorder);
            txtUsername.requestFocus();
        }

        if (error != "") {
            showError(error);
            return;
        }

        if (!password.equals(confirm)) {
            error += "Confirm password must be equal to password\n";
            txtConfirm.setBorder(redBorder);
            txtConfirm.requestFocus();
        }

        if (password.length() < 8) {
            error += "Password must be greater than or equal to 8\n";
            txtPassword.setBorder(redBorder);
            txtPassword.requestFocus();
        }

        if (error != "") {
            showError(error);
            return;
        }

        if (!userService.register(username, password, selectedCombobox().equalsIgnoreCase("Manager") ? Role.Manager : Role.Teacher)) {
            showError("Username already exists");
            txtUsername.requestFocus();
            txtUsername.setBorder(redBorder);
            return;
        }

        lblSignInMouseClicked(null);
    }

