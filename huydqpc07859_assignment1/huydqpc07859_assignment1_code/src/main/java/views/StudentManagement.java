    public void start(UserApplication user) {
        this.user = user;
        if (user == null) {
            this.setVisible(false);
            LoginFrame login = new LoginFrame();
            login.start();
            return;
        }
        if (user.getRole().equals(Role.Teacher)) {
            this.btnAdd.setVisible(false);
        }
        this.txtUsername.setText("Name: " + user.getUsername());
        this.txtRole.setText("Role: " + (user.getRole().equals(Role.Manager) ? "Manager" : "Teacher"));
        this.setLocationRelativeTo(null);
        this.setVisible(true);
        this.txtUsername.requestFocus();
        this.pnldashBoard.setVisible(true);
        this.pnlTop.setVisible(false);
        this.pnlStudents.setVisible(false);
        this.updateTableDashBoard();
        this.resetFormFind();
        new ViewThread(this.lblClock).start();
        this.btnFind.setVisible(false);
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                showSuccess("Thank you for using the program hello and see you again");
            }
        });

    }

