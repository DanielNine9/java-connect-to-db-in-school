    private void btnExportStudentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportStudentsActionPerformed
        // TODO add your handling code here:
        List<Student> list = service.top10Student();
        if (list.isEmpty()) {
            showWarn("The list is empty");

        } else {
            try {
                String path = showInputFile();
                if (path == null) {
                    return;
                }
                MyFile.writeExcel(list, path);
                showSuccess("Save file successfully");
            } catch (Exception ex) {
                showErr("Something went wrong");
                Logger.getLogger(StudentManagement.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnExportStudentsActionPerformed
    public List<Student> findByOption(
            ArrayList<Student> list,
            String id,
            String name,
            String minEn,
            String maxEn,
            String minIT,
            String maxIT,
            String minPE,
            String maxPE,
            String address) {

        return list.stream()
                .filter(a -> ((id.isEmpty() || a.getId().toLowerCase().contains(id.toLowerCase()))
                && (name.isEmpty() || a.getFullName().toLowerCase().contains(name.toLowerCase()))
                && (address.isEmpty() || a.getAddress().toLowerCase().contains(address.toLowerCase()))
                && (minEn.isEmpty() || a.getEnglish() >= Double.parseDouble(minEn))
                && (minIT.isEmpty() || a.getTech() >= Double.parseDouble(minIT))
                && (minPE.isEmpty() || a.getPhysicalEducation() >= Double.parseDouble(minPE))
                && (maxEn.isEmpty() || a.getEnglish() <= Double.parseDouble(maxEn))
                && (maxIT.isEmpty() || a.getEnglish() <= Double.parseDouble(maxIT))
                && (maxPE.isEmpty() || a.getEnglish() <= Double.parseDouble(maxPE))))
                .collect(Collectors.toList());
    }


