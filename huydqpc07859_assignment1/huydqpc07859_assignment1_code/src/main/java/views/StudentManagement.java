    private void updateTableDashBoard() {
        DefaultTableModel model = (DefaultTableModel) tblDashboard.getModel();
        model.setRowCount(0);
        List<Student> list = service.getStudents();
        lblNumberDashboard.setText("" + list.size());
        if (!list.isEmpty()) {
            for (Student x : list) {
                model.addRow(new Object[]{x.getId(), x.getFullName(), x.getEmail(), x.getPhoneNumber(), x.getSex() == 1 ? "Nam" : "Nữ"});
            }
        }

    }

    private void updateTableTop() {
        DefaultTableModel model = (DefaultTableModel) tblTop.getModel();
        model.setRowCount(0);
        int i = 0;

        List<Student> list = service.top10Student();
        if (!list.isEmpty()) {
            for (Student x : list) {
                if (i == 0) {
                    lblTop1.setText(x.getFullName());
                } else if (i == 1) {
                    lblTop2.setText(x.getFullName());
                } else if (i == 2) {
                    lblTop3.setText(x.getFullName());
                }
                ++i;
                model.addRow(new Object[]{x.getId(), x.getFullName(), x.getEnglish(), x.getTech(), x.getPhysicalEducation(), (x.getEnglish() + x.getTech() + x.getPhysicalEducation()) / 3});
            }
        }
    }

    private void updateTableStudents(List<Student> list) {
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
        model.setRowCount(0);
        this.listFind = list;
        if (!list.isEmpty()) {
            for (Student x : list) {
                model.addRow(new Object[]{x.getId(), x.getFullName(), x.getEmail(), x.getPhoneNumber(), x.getSex() == 1 ? "Nam" : "Nữ"});
            }
        }

    }

